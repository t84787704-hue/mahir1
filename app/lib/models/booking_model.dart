class BookingModel {
  final String workerName;
  final String category;
  final String date;
  final String time;
  final String address;
  final String problem;
  final String price;
  final String status;

  BookingModel({
    required this.workerName,
    required this.category,
    required this.date,
    required this.time,
    required this.address,
    required this.problem,
    required this.price,
    required this.status,
  });
}